function sampev.onSendCommand(cmd)
    if cmd:lower() == '/acs' then 
        window.v = not window.v
        return false
    elseif cmd:lower() == '/acs.show' then
        showcase()
        return false
    elseif cmd:lower():find('/acs.setslot (.+) (.+)') then
        slot, item = cmd:match('/acs.setslot (.+) (.+)')
        print('command: setslot: '..slot..' : '..item)
        setItem(slot, item)
        return false
    elseif cmd:lower():find('/acs.fight (.+)') then
        idididiid = cmd:lower():match('/acs.fight (.+)')
        if tonumber(idididiid) > 5 or tonumber(idididiid) < 0 then 
            sampAddChatMessage('/acs.fight [0 - 5]', -1)
        else
            fightstyle_selected.v = tonumber(idididiid)
            applyFightStyle()
        end
        return false
    elseif cmd:lower() == '/acs.glow' or cmd:lower() == '/acs.aura' then
        if glow.v then
            clear(6)
            glow.v = false
        else
            slotApply(6, glowEffect_data, color_list_data[1])
            glow.v = true
        end
    elseif string.find(cmd:lower(), '/acs.getlist (.+)') then
        sampAddChatMessage('не работает', -1)
        --type = cmd:match('/acs.getlist (.+)')
        --sampAddChatMessage('/acs.getlist '..type, -1)
        return false
        --[[
        if #type ~= 0 then
            sampAddChatMessage('Getting list...', -1)
            getList(tonumber(type))
            return false
        end]]
    end
    
end

